                 <h3>Actividad 1: Manualidades con materiales reciclable</h3>
                 <p>Descubre cómo hacer manualidades utilizando materiales reciclados.</p>
                  <ul><li>Pasos para hacer una maceta con botellas de plastico</li></ul>
                  <ul><li>Materiales necesarios</li></ul>
                  <br>
                  Así es, a continuación aprenderemos a hacer macetas recicladas con botellas de plástico. Además, plantaremos algunas semillas en las macetas, lo que servirá a los más pequeños a interesarse por la naturaleza y aprender a cuidarla y a respetarla.
                  <br>
                  <ol>
                    <li>Una botella de plástico usada (por cada maceta se necesita una botella)</li> 
                     <li>Algunas tiras de tela absorbente (puedes obtenerlas de trapos rotos o ropa vieja)</li> 
                     <li>Sustrato universal o tierra de jardín</li> 
                     <li>Semillas</li> 
                       <li>Unas tijeras</li>
                  </ol>
                  <br>
                  <ul><li>PASOS PARA REALIZAR TU MACETA</li></ul>
                  <ul>Paso 1. En primer lugar, corta la botella de plástico en dos mitades trazando una circunferencia a media altura. Si eres el peque de la casa, pide ayuda a alguien mayor para que corte la botella.
                    Comprueba que las dos mitades de la botella encajen bien una con otra: la parte inferior servirá como depósito para el agua de riego y la parte superior contendrá el sustrato para la planta. </ul>
                    <ul>Paso 2. Coloca dos tiras de tela absorbente en el cuello de la botella, como se muestra en la imagen, para que el sustrato pueda absorber el agua a través de ellas.</ul>
                    <ul>Paso 3. Rellena la botella con tierra de jardín o sustrato universal. Si las tiras de tela no son suficientes para retener la tierra, puedes colocar algodón en el estrechamiento de la botella, pero asegúrate de que las tiras de tela estén en contacto con el agua de la parte inferior de la maceta.</ul>
                    <ul>Paso 4. Planta las semillas en tu nueva maceta, presionando ligeramente con el dedo hasta que estén parcialmente enterradas en el sustrato. Puedes utilizar lentejas, garbanzos u otras semillas, en la variedad está el gusto. </ul>
                    <ul>Paso 5. Y listo, ¡ya puedes disfrutar de tu maceta reciclada! No olvides cuidar las semillas para que germinen y crezcan los brotes.</ul>

                    <ul><li>Como ves, es muy fácil y divertido contribuir a cuidar el medio ambiente con el reciclaje creativo. Anímate y cuéntanos tus experiencias y tus ideas creativas para reciclar.</li></ul>
