                     <h3>Actividad 4: Portalapices</h3>
                      <p>Descubre cómo hacer manualidades utilizando materiales reciclados.</p>
                     <ul><li>Pasos para hacer un portalapices</li></ul>
                      <ul><li>Materiales necesarios</li></ul>
                       <br>
                      Los materiales necesarios para fabricar una guitarra de cartón son bastante sencillos de encontrar y utilizar. Los básicos son los siguientes:
                      <br>
                     <ol>
                      <li>Botellas vacías de Fabuloso.</li>
                      <li>Marcadores.</li>
                      <li>Bisturí / Tijeras.</li>
                      <li>Pinturas acrílicas de tus colores favoritos.</li>
                      <li>Pincel o brocha gruesos.</li>
                      </ol>
                      <br>
                      <ul><li>PASOS PARA REALIZAR TU PORTALAPICES</li></ul>
                       <ul>Paso1: Para comenzar tus manualidades paso a paso solo debes quitar la etiqueta que tiene la botella, teniendo en cuenta que no quede ninguna marca de pegante o adhesivo.</ul>
                      <ul>Paso2: Enseguida, con ayuda de un marcador en la mitad de la botella, traza la silueta de las orejas de un gato o el animal que desees, tal cual como lo ves en la imagen.</ul>
                      <ul>Paso3: Con ayuda de un bisturí corta la botella por donde trazaste la silueta, así iras notando como toma forma el portalápiz.</ul>
                      <ul>Paso4: Toma la botella cortada y píntala con ayuda de una brocha o pincel. Aquí puedes dejar volar tu imaginación y alternar varios colores en tus manualidades paso a paso.</ul>
                      <ul>Paso5: Deja secar por unos minutos. Con ayuda del marcador pinta la cara del gato; dibuja ojos, boca y algunos bigotes. En este paso será indispensable la ayuda de los más pequeños del hogar, ellos aman este tipo de manualidades.</ul>
