                    <h3>Actividad 2: Manualidades con Material Reciclado</h3>
                   <p>Descubre cómo hacer manualidades utilizando materiales reciclados.</p>
                     <ul><li>Pasos para hacer una guitarra con cartón</li></ul>
                   <ul><li>Materiales necesarios</li></ul>
                  <br>
                   Los materiales necesarios para fabricar una guitarra de cartón son bastante sencillos de encontrar y utilizar. Los básicos son los siguientes:
                 <br>
                 <ol>
                 <li>Una o varias cajas de cartón de tamaño medio.</li>
                 <li>Cúter o tijeras.</li>
                   <li>Gomas elásticas o cuerdas de colores.</li>
                   <li>Pegamento o cola de contacto.</li>
                   <li>Pinturas.</li>
                   <li>Punzón. Aunque también puedes utilizar el cúter, esta herramienta te ayudará de forma precisa en alguno de los pasos.</li>
                   </ol>
                   <br>
                    <ul><li>PASOS PARA REALIZAR TU GUITARRA</li></ul>
                   <ul>Paso 1. Utiliza una plantilla de diseño: puedes hacerla con la forma de guitarra que más te guste o buscar alguna en internet. </ul>
                   <ul>Paso 2. Sigue la plantilla de diseño para recortar varias piezas similares del modelo de guitarra.</ul>
                   <ul>Paso 3. Utiliza el pegamento o la cola de contacto para pegar estas siluetas y conseguir una estructura adecuada. Si las siluetas son muy finas, puedes utilizar algún tipo de cartón de relleno para darle más volumen al diseño.</ul>
                   <ul>Paso 4. Una vez pegadas, realiza un agujero central para simular la caja de resonancia. Utiliza un cúter, mejor que unas tijeras, ya que el resultado será mucho mejor. También es aconsejable dibujar a lápiz el círculo sobre el que se realizará el corte.</ul>
                   <ul>Paso 5. En la pieza superior, una vez realizados los pasos anteriores, realiza cuatro pequeños agujeros en el cuerpo de la guitarra y añade gomas de colores para simular las cuerdas de la guitarra. Puedes añadir una nueva pieza sobre ella para asegurar las cuerdas y que el diseño quede más estético. Realiza el mismo proceso en el mástil de la guitarra y une las cuerdas en ambos puntos.</ul>
